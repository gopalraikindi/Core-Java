package Durga.youtube.tutorials.java;

//length vs length()

//length: it represent the size of array
//length(): it used only for strings not for array type of strings


public class A7arrays3b {

    public static void main(String[] args) {

        int[] a = new int[10]; // Declared the array
        System.out.println(a.length);

        String sb="daya";
        System.out.println(sb.length());

        String[] s={"gopal"};
        System.out.println(s.length);

        String[] sa={"gopal","rahul"};
        System.out.println(sa.length);



    }
}

