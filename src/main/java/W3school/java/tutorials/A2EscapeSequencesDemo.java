package W3school.java.tutorials;

//     \n	New Line
//     \r	Carriage Return
//     \t	Tab
//     \b	Backspace

public class A2EscapeSequencesDemo {
    public static void main(String[] args) {
        System.out.println("This is a new line character:\nNext line starts here.");
        System.out.println("This is a carriage return character:\rCarriage return overwrites this line.");
        System.out.println("This is a tab character:\tTab space added here.");
        System.out.println("This is a backspace character:Backspace\b removed the previous character.");
    }
}

